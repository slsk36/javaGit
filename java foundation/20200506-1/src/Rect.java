
public class Rect {
	
	private int width;
	private int height;
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
//	alt+shift+s 누르고 r을 눌러서 자동완성 정보은닉
}
